String convertMonth(month) {
  if (month == 'มกราคม') {
    return "1";
  }
  if (month == 'กุมภาพันธ์') {
    return "2";
  }
  if (month == 'มีนาคม') {
    return "3";
  }
  if (month == 'เมษายน') {
    return "4";
  }
  if (month == 'พฤษภาคม') {
    return "5";
  }
  if (month == 'มิถุนายน') {
    return "6";
  }
  if (month == 'กรกฎาคม') {
    return "7";
  }
  if (month == 'สิงหาคม') {
    return "8";
  }
  if (month == 'กันยายน') {
    return "9";
  }
  if (month == 'ตุลาคม') {
    return "10";
  }
  if (month == 'พฤศจิกายน') {
    return "11";
  }
  if (month == 'ธันวาคม') {
    return "12";
  }
  return "0";
}
